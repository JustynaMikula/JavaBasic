package org.example;

public class Ex2
{
    public static void main(String[] args) {
        int x = 200;
        int y = 60;
        System.out.println("x + y = "+(x+y));
        System.out.println("x - y = "+(x-y));
        System.out.println("x * y = "+(x*y));
        System.out.println("x / y = "+(x/y));
        System.out.println("x == y = "+(x==y));
        System.out.println("x < y = "+(x<y));
        System.out.println("x > y = "+(x>y));
        System.out.println("x >= y = "+(x>=y));
        System.out.println("x <= y = "+(x<=y));
        System.out.println("x y++ "+(y++));
        System.out.println("x ++y"+(++y));
 }}
